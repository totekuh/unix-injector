/* Round constants (precomputed for AES-128) */
static const uint8_t aes_rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

/* GF(2^8) multiplication with irreducible polynomial x^8+x^4+x^3+x+1 */
static uint8_t aes_gmult(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1)
            p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi)
            a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

/* Key expansion (AES-128: 16-byte key → 176-byte expanded key) */
static void aes_key_expansion(const uint8_t key[16], uint8_t w[AES_EXPANDED_KEY_SIZE]) {
    uint8_t tmp[4];

    for (int i = 0; i < AES_NK; i++) {
        w[4*i+0] = key[4*i+0];
        w[4*i+1] = key[4*i+1];
        w[4*i+2] = key[4*i+2];
        w[4*i+3] = key[4*i+3];
    }

    for (int i = AES_NK; i < AES_NB * (AES_NR + 1); i++) {
        tmp[0] = w[4*(i-1)+0];
        tmp[1] = w[4*(i-1)+1];
        tmp[2] = w[4*(i-1)+2];
        tmp[3] = w[4*(i-1)+3];

        if (i % AES_NK == 0) {
            uint8_t t = tmp[0];
            tmp[0] = aes_sbox[tmp[1]];
            tmp[1] = aes_sbox[tmp[2]];
            tmp[2] = aes_sbox[tmp[3]];
            tmp[3] = aes_sbox[t];
            tmp[0] ^= aes_rcon[i / AES_NK];
        }

        w[4*i+0] = w[4*(i-AES_NK)+0] ^ tmp[0];
        w[4*i+1] = w[4*(i-AES_NK)+1] ^ tmp[1];
        w[4*i+2] = w[4*(i-AES_NK)+2] ^ tmp[2];
        w[4*i+3] = w[4*(i-AES_NK)+3] ^ tmp[3];
    }
}

static void aes_add_round_key(uint8_t state[16], const uint8_t *w, int round) {
    for (int c = 0; c < AES_NB; c++) {
        state[AES_NB*0+c] ^= w[4*AES_NB*round + 4*c + 0];
        state[AES_NB*1+c] ^= w[4*AES_NB*round + 4*c + 1];
        state[AES_NB*2+c] ^= w[4*AES_NB*round + 4*c + 2];
        state[AES_NB*3+c] ^= w[4*AES_NB*round + 4*c + 3];
    }
}

static void aes_inv_sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; i++)
        state[i] = aes_inv_sbox[state[i]];
}

static void aes_inv_shift_rows(uint8_t state[16]) {
    uint8_t tmp;
    /* Row 1: right by 1 */
    tmp = state[AES_NB*1+3];
    state[AES_NB*1+3] = state[AES_NB*1+2];
    state[AES_NB*1+2] = state[AES_NB*1+1];
    state[AES_NB*1+1] = state[AES_NB*1+0];
    state[AES_NB*1+0] = tmp;
    /* Row 2: right by 2 (swap pairs) */
    tmp = state[AES_NB*2+0]; state[AES_NB*2+0] = state[AES_NB*2+2]; state[AES_NB*2+2] = tmp;
    tmp = state[AES_NB*2+1]; state[AES_NB*2+1] = state[AES_NB*2+3]; state[AES_NB*2+3] = tmp;
    /* Row 3: right by 3 = left by 1 */
    tmp = state[AES_NB*3+0];
    state[AES_NB*3+0] = state[AES_NB*3+1];
    state[AES_NB*3+1] = state[AES_NB*3+2];
    state[AES_NB*3+2] = state[AES_NB*3+3];
    state[AES_NB*3+3] = tmp;
}

static void aes_inv_mix_columns(uint8_t state[16]) {
    for (int j = 0; j < AES_NB; j++) {
        uint8_t c[4];
        for (int i = 0; i < 4; i++)
            c[i] = state[AES_NB*i + j];

        state[AES_NB*0+j] = aes_gmult(0x0e,c[0]) ^ aes_gmult(0x0b,c[1]) ^ aes_gmult(0x0d,c[2]) ^ aes_gmult(0x09,c[3]);
        state[AES_NB*1+j] = aes_gmult(0x09,c[0]) ^ aes_gmult(0x0e,c[1]) ^ aes_gmult(0x0b,c[2]) ^ aes_gmult(0x0d,c[3]);
        state[AES_NB*2+j] = aes_gmult(0x0d,c[0]) ^ aes_gmult(0x09,c[1]) ^ aes_gmult(0x0e,c[2]) ^ aes_gmult(0x0b,c[3]);
        state[AES_NB*3+j] = aes_gmult(0x0b,c[0]) ^ aes_gmult(0x0d,c[1]) ^ aes_gmult(0x09,c[2]) ^ aes_gmult(0x0e,c[3]);
    }
}

/* Decrypt one 16-byte block */
static void aes_inv_cipher(const uint8_t in[16], uint8_t out[16], const uint8_t *w) {
    uint8_t state[16];

    /* Column-major load */
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < AES_NB; j++)
            state[AES_NB*i+j] = in[i + 4*j];

    aes_add_round_key(state, w, AES_NR);

    for (int r = AES_NR - 1; r >= 1; r--) {
        aes_inv_shift_rows(state);
        aes_inv_sub_bytes(state);
        aes_add_round_key(state, w, r);
        aes_inv_mix_columns(state);
    }

    aes_inv_shift_rows(state);
    aes_inv_sub_bytes(state);
    aes_add_round_key(state, w, 0);

    /* Column-major store */
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < AES_NB; j++)
            out[i + 4*j] = state[AES_NB*i+j];
}

/*
 * Decrypt AES-128-ECB, strip PKCS#7 padding.
 * in/out may alias (in-place). Returns plaintext length, -1 on error.
 */
static int aes_decrypt_ecb(const uint8_t *in, size_t len,
                           const uint8_t key[16], uint8_t *out) {
    if (len == 0 || len % AES_BLOCK_SIZE != 0)
        return -1;

    uint8_t w[AES_EXPANDED_KEY_SIZE];
    aes_key_expansion(key, w);

    for (size_t i = 0; i < len; i += AES_BLOCK_SIZE)
        aes_inv_cipher(in + i, out + i, w);

    /* PKCS#7 unpad */
    uint8_t pad = out[len - 1];
    if (pad == 0 || pad > AES_BLOCK_SIZE)
        return -1;
    for (uint8_t i = 0; i < pad; i++)
        if (out[len - 1 - i] != pad)
            return -1;

    return (int)(len - pad);
}
